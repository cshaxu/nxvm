static COLORREF CharProp2Color(UCHAR prop, BOOL flagForeColor) {
    UCHAR byte;
    if (flagForeColor) {
        byte = (prop & 0x0f); /* fore color */
    } else {
        byte = ((prop & 0xf0) >> 4);  /* back color */
    }
    switch (byte) {
    case 0x00:
        return COLOR_BLACK;
        break;
    case 0x01:
        return COLOR_BLUE;
        break;
    case 0x02:
        return COLOR_GREEN;
        break;
    case 0x03:
        return COLOR_CYAN;
        break;
    case 0x04:
        return COLOR_RED;
        break;
    case 0x05:
        return COLOR_MAGENTA;
        break;
    case 0x06:
        return COLOR_BROWN;
        break;
    case 0x07:
        return COLOR_LIGHTGRAY;
        break;
    case 0x08:
        return COLOR_GRAY;
        break;
    case 0x09:
        return COLOR_LIGHTBLUE;
        break;
    case 0x0a:
        return COLOR_LIGHTGREEN;
        break;
    case 0x0b:
        return COLOR_LIGHTCYAN;
        break;
    case 0x0c:
        return COLOR_LIGHTRED;
        break;
    case 0x0d:
        return COLOR_LIGHTMAGENTA;
        break;
    case 0x0e:
        return COLOR_YELLOW;
        break;
    case 0x0f:
        return COLOR_WHITE;
        break;
    default:
        return COLOR_BLACK;
        break;
    }
}

#define FONT_WIDTH  8
#define FONT_HEIGHT 16
#define FONT_NCHAR  256
#define FONT_NCOLOR 256 /* 128 */
static HDC hdcFont;
static HBITMAP hBmpFont;
static BOOL bFontCharExist[FONT_NCHAR][FONT_NCOLOR];

static VOID CreateBitmapFontChar(UCHAR ch, UCHAR prop) {
    UCHAR i, j;
    COLORREF fc, bc;
    HDC hdcChar;
    HBITMAP hBmpChar;
    HGDIOBJ hOldGdiObj;
    hdcChar = CreateCompatibleDC(NULL);
    hBmpChar = CreateCompatibleBitmap(hdcWnd, FONT_WIDTH, FONT_HEIGHT);
    hOldGdiObj = SelectObject(hdcChar, hBmpChar);
    /* prop &= 0x7f; */
    fc = CharProp2Color(prop, TRUE);
    bc = CharProp2Color(prop, FALSE);
    for (i = 0; i < FONT_HEIGHT; ++i) {
        for (j = 0; j < FONT_WIDTH; ++j) {
            if (!!(fontBitmap[ch][i] & (1 << j))) {
                SetPixel(hdcChar, j, i, fc);
            } else {
                SetPixel(hdcChar, j, i, bc);
            }
        }
    }
    BitBlt(hdcFont, ch * FONT_WIDTH, prop * FONT_HEIGHT, FONT_WIDTH, FONT_HEIGHT, hdcChar, 0, 0, SRCCOPY);
    SelectObject(hdcChar, hOldGdiObj);
    DeleteObject(hBmpChar);
    DeleteDC(hdcChar);
    bFontCharExist[ch][prop] = TRUE;
}

VOID w32adispInit() {
    UINT i, j;
    hdcWnd = GetDC(w32aHWnd);
    hdcBuf = CreateCompatibleDC(NULL);
    hBmpBuf = NULL;
    clientHeight = 0;
    clientWidth  = 0;
    flashCount   = 0;
    flashInterval = 5;
    w32adispSetScreen();
    hdcFont = CreateCompatibleDC(NULL);
    hBmpFont = CreateCompatibleBitmap(hdcWnd, FONT_WIDTH * FONT_NCHAR, FONT_HEIGHT * FONT_NCOLOR);
    SelectObject(hdcFont, hBmpFont);
    for (i = 0; i < FONT_NCHAR; ++i) {
        for (j = 0; j < FONT_NCOLOR; ++j) {
            bFontCharExist[i][j] = FALSE;
        }
    }
}

VOID w32adispSetScreen() {
    RECT clientRect,windowRect;
    LONG widthOffset, heightOffset;
    sizeRow = deviceConnectDisplayGetRowSize();
    sizeCol = deviceConnectDisplayGetColSize();
    GetClientRect(w32aHWnd,&clientRect);
    GetWindowRect(w32aHWnd,&windowRect);

    /* fetch window and customer area size to decide window side */
    widthOffset = windowRect.right - windowRect.left - clientRect.right;
    heightOffset = windowRect.bottom - windowRect.top - clientRect.bottom;
    MoveWindow(w32aHWnd, windowRect.left, windowRect.top, sizeRow * FONT_WIDTH + widthOffset,
               sizeCol * FONT_HEIGHT + heightOffset, SWP_NOMOVE);
    GetClientRect(w32aHWnd,&clientRect);
    clientHeight = clientRect.bottom - clientRect.top;
    clientWidth  = clientRect.right - clientRect.left;
    hBmpBuf = CreateCompatibleBitmap(hdcWnd,
                                     GetDeviceCaps(hdcWnd, HORZRES), GetDeviceCaps(hdcWnd, VERTRES));
    SelectObject(hdcBuf, hBmpBuf);
    w32adispPaint(TRUE);
}

static VOID DisplayCursor() {
    HBRUSH hBrush;
    HGDIOBJ hOldGdiObj;
    RECT rect;
    INT x1_cursor, y1_cursor, x2_cursor, y2_cursor;
    x1_cursor = x2_cursor =
                    deviceConnectDisplayGetCurrentCursorPosX() * FONT_HEIGHT; /* + FONT_HEIGHT / 2 */;
    cursorTop = deviceConnectDisplayGetCursorTop();
    cursorBottom = deviceConnectDisplayGetCursorBottom();
    x1_cursor += (cursorTop % 8) * FONT_HEIGHT / 8;
    x2_cursor += (cursorBottom % 8) * FONT_HEIGHT / 8;
    y1_cursor = (deviceConnectDisplayGetCurrentCursorPosY() + 0) * FONT_WIDTH;
    y2_cursor = (deviceConnectDisplayGetCurrentCursorPosY() + 1) * FONT_WIDTH;
    rect.left = y1_cursor;
    rect.top = x1_cursor;
    rect.right = y2_cursor;
    rect.bottom = x2_cursor;
    hBrush = CreateSolidBrush(COLOR_LIGHTGRAY);
    hOldGdiObj = SelectObject(hdcWnd, hBrush);
    FillRect(hdcWnd, &rect, hBrush);
    SelectObject(hdcWnd, hOldGdiObj);
    DeleteObject(hBrush);
}

VOID w32adispPaint(BOOL flagForce) {
    UCHAR i, j, ch, prop;
    flashCount = (flashCount + 1) % 10;
    if (flagForce || deviceConnectDisplayGetBufferChange() || deviceConnectDisplayGetCursorChange()) {
        for (i = 0; i < sizeCol; ++i) {
            for (j = 0; j < sizeRow; ++j) {
                ch = deviceConnectDisplayGetCurrentChar(i, j);
                prop = deviceConnectDisplayGetCurrentCharProp(i, j); /* & 0x7f; */
                if (!bFontCharExist[ch][prop]) {
                    CreateBitmapFontChar(ch, prop);
                }
                BitBlt(hdcBuf, j * FONT_WIDTH, i * FONT_HEIGHT, FONT_WIDTH, FONT_HEIGHT,
                       hdcFont, ch * FONT_WIDTH, prop * FONT_HEIGHT, SRCCOPY);
            }
        }
    }
    BitBlt(hdcWnd, 0, 0, clientWidth, clientHeight, hdcBuf, 0, 0, SRCCOPY);
    if (deviceConnectDisplayGetCursorVisible() && ((flashCount % 10) < flashInterval)) {
        DisplayCursor();
    }
}

VOID w32adispFinal() {
    DeleteObject(hBmpFont);
    DeleteDC(hdcFont);
    DeleteObject(hBmpBuf);
    DeleteDC(hdcBuf);
}
